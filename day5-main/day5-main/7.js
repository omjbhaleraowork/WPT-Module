function redColor() {
  let ptag = document.querySelector("p");
  ptag.style.color = "red";
}

function greenColor() {
  let ptag = document.querySelector("p");
  ptag.style.color = "green";
}

function blueColor() {
  let ptag = document.querySelector("p");
  ptag.style.color = "blue";
}

function resetColor() {
  let ptag = document.querySelector("p");
  ptag.style.color = "";
}
