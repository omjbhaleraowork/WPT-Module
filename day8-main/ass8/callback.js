// Basic for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
  }
  
  // Advanced for loop
  const arr = [1, 2, 3, 4, 5];
  for (const element of arr) {
    console.log(element);
  }
  
  // forEach with callback
  const array = [1, 2, 3, 4, 5];
  array.forEach((element) => {
    console.log(element);
  });
  